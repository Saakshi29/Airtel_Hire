package springs.model;

public class Response {
Long id;
String type;
String status;



public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public Long getId() {
	return id;
}
public void setId(Long id) {
	this.id = id;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}

}
